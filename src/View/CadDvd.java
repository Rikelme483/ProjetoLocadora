    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        // TODO add your handling code here:
        
        String filme = campoFilme.getText();
        
        if(filme.isEmpty()) {
            JOptionPane.showMessageDialog(this, "O campo n√£o pode estar vazio!");
        } else {
            
            DAO.DAO_DVD objDvd = new DAO_DVD();
            Model.DVD dvd = new DVD(codigoGlobal);
            
            try {
                objDvd.adiciona(dvd);
                campoFilme.setText("");
                JOptionPane.showMessageDialog(null, "DVD inserido com sucesso!");
            } catch(HeadlessException error) {
                JOptionPane.showMessageDialog(this, "Houve um erro! \n " + error.getMessage());
            }
            
        }
        
    }//GEN-LAST:event_btCadastrarActionPerformed

