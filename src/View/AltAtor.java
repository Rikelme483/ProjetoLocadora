    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        // TODO add your handling code here:
        
        String nome_ator = campoNome.getText();
        String sexo = (String) comboSexo.getSelectedItem();
        String dt_nasc = campoNascimento.getText();
        
        if(nome_ator.isEmpty() || dt_nasc.isEmpty() ) {
            JOptionPane.showMessageDialog(this, "Os campos n√£o podem estar vazios!");
        } else {
            
            DAO.DAO_Atores objAtor = new DAO_Atores();
            Model.Atores ator = new Atores(nome_ator, sexo, dt_nasc, codigoGlobal);
            
            try {
                objAtor.altera(ator);
                campoNome.setText("");
                campoNascimento.setText("");
                comboSexo.setSelectedIndex(0);
            } catch(HeadlessException error) {
                JOptionPane.showMessageDialog(this, "Houve um erro ao alterar ator" + error.getMessage() );
            }
            
        }
        
    }//GEN-LAST:event_btAlterarActionPerformed

