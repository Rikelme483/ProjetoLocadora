    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        // TODO add your handling code here:
        
        String nome_diretor = campoNome.getText();
        String sexo = (String) comboSexo.getSelectedItem();
        String nacionalidade = campoNacionalidade.getText();
        
        if(nome_diretor.isEmpty() || nacionalidade.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Os campos n√£o podem estar vazios!");
        } else {
            
            DAO.DAO_Diretores objDiretor = new DAO_Diretores();
            Model.Diretor diretor = new Diretor(nome_diretor, sexo, nacionalidade, codigoGlobal);
            
            try{
                objDiretor.altera(diretor);
                campoNome.setText("");
                campoNacionalidade.setText("");
                comboSexo.setSelectedIndex(0);
                JOptionPane.showMessageDialog(this, "Diretor alterado com sucesso!");
            } catch(HeadlessException error) {
                JOptionPane.showMessageDialog(this, "Houve um erro \n " + error.getMessage());
            }
            
        }
        
    }//GEN-LAST:event_btAlterarActionPerformed

