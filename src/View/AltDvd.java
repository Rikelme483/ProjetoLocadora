    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        // TODO add your handling code here:
        
        String filme = Integer.toString(comboFilme.getSelectedIndex()+ 1);
        
        DAO.DAO_DVD objDvd = new DAO_DVD();
        
        Model.DVD dvd = new DVD(filme, codigoGlobal);
        
        try {
            
            objDvd.altera(dvd);
            comboFilme.setSelectedIndex(0);
            JOptionPane.showMessageDialog(this, "DVD alterado com sucesso!");
        } catch(HeadlessException error) {
            JOptionPane.showMessageDialog(this, "Houve um erro ao alterar! \n " + error.getMessage());
        }
        
        
    }//GEN-LAST:event_btAlterarActionPerformed

