    private void btAlterar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterar1ActionPerformed
        
        String codigo, fk_id_ator, fk_id_filme, nome_ator, nome_filme;
        
        if(tblAtorFilme.getSelectedRow() < 0 ) {
            JOptionPane.showMessageDialog(this, "Selecione um campo para alterar!");
        } else {
            
            int linha = tblAtorFilme.getSelectedRow();
            
            codigo = (String) tblAtorFilme.getValueAt(linha, 0);
            fk_id_ator = (String) tblAtorFilme.getValueAt(linha, 1);
            fk_id_filme = (String) tblAtorFilme.getValueAt(linha, 2);
            nome_ator = (String) tblAtorFilme.getValueAt(linha, 3);
            nome_filme = (String) tblAtorFilme.getValueAt(linha, 4);
            
            AltAtorFilme altera = new AltAtorFilme();
            altera.setVisible(true);
            
            altera.resgate(fk_id_ator, fk_id_filme, nome_ator, nome_filme, codigo);
            this.dispose();
            
        }

        

    }//GEN-LAST:event_btAlterar1ActionPerformed

    private void btExcluir2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluir2ActionPerformed
        // TODO add your handling code here:
        
        if(tblAtorFilme.getSelectedRow() < 0 ) {
            JOptionPane.showMessageDialog(this, "Selecione um campo para excluir!");
        } else {
            
            int linha = tblAtorFilme.getSelectedRow();
            
            String id = (String) tblAtorFilme.getValueAt(linha, 0);
            
            DAO.DAO_AtorFilme objAtorFilme = new DAO_AtorFilme();
            Model.AtorFilme atorFilme = new AtorFilme(id);
            
            int confirm = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir este dado?");
            
            if(confirm == JOptionPane.YES_OPTION) {
                
                try {
                    objAtorFilme.exclui(atorFilme);
                    JOptionPane.showMessageDialog(this, "Filme excluido");
                } catch(HeadlessException error) {
                    JOptionPane.showMessageDialog(this, "Houve um erro ao excluir filme" + error.getMessage());
                }
                
            } else {
                JOptionPane.showMessageDialog(this, "Dado não excluido");
            }
        }
       
        listaAtorFilme();

    }//GEN-LAST:event_btExcluir2ActionPerformed

