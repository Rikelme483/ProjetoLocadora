    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        // TODO add your handling code here:
        
        String titulo = campoTitulo.getText();
        String genero = Integer.toString(comboCategoria.getSelectedIndex() + 1 );
        String classificacao = (String) comboClassificacao.getSelectedItem();
        String ano_lancamento = campoLancamento.getText();
        String duracao = campoDuracao.getText();
        String diretor = Integer.toString(comboDiretor.getSelectedIndex() + 1 );
        
        if(titulo.isEmpty() || ano_lancamento.isEmpty() || duracao.isEmpty() ) {
            JOptionPane.showMessageDialog(this, "Os campo não podem estar vazios!");
        } else {
            
            Model.Filmes filmes = new Filmes(titulo, ano_lancamento, duracao, classificacao, diretor, genero, codigoGlobal);
            DAO.DAO_Filmes objFilme = new DAO_Filmes();
            
            try {
                objFilme.altera(filmes);
                JOptionPane.showMessageDialog(null, "Filme alterado com sucesso!");
                campoTitulo.setText("");
                campoLancamento.setText("");
                campoDuracao.setText("");
                comboCategoria.setSelectedIndex(0);
                comboClassificacao.setSelectedIndex(0);
                comboDiretor.setSelectedIndex(0);
            } catch(HeadlessException error) {
                JOptionPane.showMessageDialog(null, "Houve um erro ao inserir - " + error.getMessage());
            }
            
        }
        
    }//GEN-LAST:event_btAlterarActionPerformed

