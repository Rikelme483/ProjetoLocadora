    private void listaAtorFilme() {
        DefaultTableModel tabela = (DefaultTableModel) tblAtorFilme.getModel();
        tabela.setNumRows(0);
        
        DAO.DAO_AtorFilme objAtorFilme = new DAO_AtorFilme();
        ResultSet dados = objAtorFilme.listaAtorFilme();
        
        try {
            while(dados.next()) {
                tabela.addRow( new Object[] {
                   dados.getString(1),
                   dados.getString(2),
                   dados.getString(3),
                   dados.getString(4),
                   dados.getString(5),
                });
            }
        } catch(SQLException error) {
            throw new RuntimeException(error.getMessage());
        }
    }
    
    private void pesquisaAtorFilme() {
        
        String pesquisa = campoPesquisa.getText();
        DefaultTableModel tabela = (DefaultTableModel) tblAtorFilme.getModel();
        tabela.setNumRows(0);
        
        DAO.DAO_AtorFilme objAtorFilme = new DAO_AtorFilme();
        ResultSet dados = objAtorFilme.pesquisaAtorFilme(pesquisa);
        
        try {
            while(dados.next()) {
                tabela.addRow( new Object[] {
                   dados.getString(1),
                   dados.getString(2),
                   dados.getString(3),
                   dados.getString(4),
                   dados.getString(5),
                });
            }
        } catch(SQLException error) {
            throw new RuntimeException(error.getMessage());
        }
    }

