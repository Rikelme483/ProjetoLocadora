    private void listaFilmes() {
        DefaultTableModel tabelaFilme = (DefaultTableModel) tblAtores.getModel();
        tabelaFilme.setNumRows(0);
        
        DAO.DAO_Atores objFilmes = new DAO_Atores();
        
        ResultSet dados = objFilmes.listar();
        
        try {
            while(dados.next()) {
                tabelaFilme.addRow( new Object[] {
                   dados.getString(1),
                   dados.getString(2),
                   dados.getString(3),
                   dados.getString(4)
                });
            }
        } catch(SQLException error) {
            throw new RuntimeException(error.getMessage());
        }
    }
    
    private void pesquisaAtor() {
        
        String pesquisa = campoPesquisa.getText();
        DefaultTableModel tabelaFilme = (DefaultTableModel) tblAtores.getModel();
        tabelaFilme.setNumRows(0);
        
        DAO.DAO_Atores objFilmes = new DAO_Atores();
        
        ResultSet dados = objFilmes.pesquisarAtor(pesquisa);
        
        try {
            while(dados.next()) {
                tabelaFilme.addRow( new Object[] {
                   dados.getString(1),
                   dados.getString(2),
                   dados.getString(3),
                   dados.getString(4)
                });
            }
        } catch(SQLException error) {
            throw new RuntimeException(error.getMessage());
        }
        
    }

