    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        
        if(tblAtores.getSelectedRow() < 0 ) {
            JOptionPane.showMessageDialog(this, "Selecione um campo para excluir!");
        } else {
            int linha = tblAtores.getSelectedRow();
            String id = (String) tblAtores.getValueAt(linha, 0);
            
            DAO.DAO_Atores objAtor = new DAO_Atores();
            Model.Atores ator = new Atores(id);
            
            int confirm = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir este ator?");
            
            if(confirm == JOptionPane.YES_OPTION ) {
                try {
               
                objAtor.excluir(ator);
                JOptionPane.showMessageDialog(this, "Ator excluÃ­do!");
                
            } catch(HeadlessException error) {
                JOptionPane.showMessageDialog(this, "Houve um erro ao excluir ator" + error.getMessage() );
            }
            } else {
                JOptionPane.showMessageDialog(this, "Dado nÃ£o foi excluido.");
            }
            
            
            
            listaFilmes();
            
        }
        
    }//GEN-LAST:event_btExcluirActionPerformed

