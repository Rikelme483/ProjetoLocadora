    private void comboCategoria() {
        DAO.DAO_Generos objGenero = new DAO_Generos();
        ResultSet lista = objGenero.listar();
        
        try {
            while(lista.next()) {
                comboCategoria.addItem(lista.getString(2));
            }
        } catch(SQLException error) {
            throw new RuntimeException(error.getMessage());
        }
        
    }
    
    private void comboDiretor() {
        DAO.DAO_Diretores objDiretor = new DAO_Diretores();
        ResultSet rs = objDiretor.listaCadastrarAlterar();
        
        try {
            while(rs.next()) {
                comboDiretor.addItem(rs.getString(2));
            }
        } catch(SQLException error) {
            throw new RuntimeException(error.getMessage());
        }
        
    }
    
