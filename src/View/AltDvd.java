    private void comboFilme() {
        DAO.DAO_Filmes objFilme = new DAO_Filmes();
        ResultSet lista = objFilme.listarFilmeAtor();
        
        try {
            while(lista.next()) {
                comboFilme.addItem(lista.getString(2));
            }
        } catch(SQLException error) {
            throw new RuntimeException(error.getMessage());
        }
        
    }
    
    String codigoGlobal;
    String fk_idFilme_global;
    
    public void resgate(String fk_id_filme, String nome_filme, String codigo) {
        codigoGlobal = codigo;
        comboFilme.setSelectedItem(nome_filme);
        fk_idFilme_global = fk_id_filme;
    }
    
    
    

